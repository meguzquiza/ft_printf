#include "../ft_printf.h"

int	ft_putstr(char* str)
{
	int	x;

	x = 0;
	if (str[x] == '\0')
	{
		write (1, "(NULL)", 6);
		return (6);
	}
	while (str[x] != '\0')
	{
		write (1, &str[x], 1);
		x++;
	}
	return(x);
}
